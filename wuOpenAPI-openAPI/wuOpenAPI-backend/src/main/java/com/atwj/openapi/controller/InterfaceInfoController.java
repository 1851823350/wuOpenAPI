package com.atwj.openapi.controller;

import com.atwj.openapi.annotation.AuthCheck;
import com.atwj.openapi.common.*;
import com.atwj.openapi.constant.CommonConstant;
import com.atwj.openapi.exception.BusinessException;
import com.atwj.openapi.model.dto.InterfaceInfo.InterfaceInfoAddRequest;
import com.atwj.openapi.model.dto.InterfaceInfo.InterfaceInfoInvokeRequest;
import com.atwj.openapi.model.dto.InterfaceInfo.InterfaceInfoQueryRequest;
import com.atwj.openapi.model.dto.InterfaceInfo.InterfaceInfoUpdateRequest;
import com.atwj.apicommon.model.entity.InterfaceInfo;
import com.atwj.apicommon.model.entity.User;
